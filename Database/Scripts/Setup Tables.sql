-- 1. HOLIDAY MANAGEMENT TABLE
CREATE TABLE PUBLIC_HOLIDAYS (
    holiday_id          NUMBER(10)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    holiday_date        DATE            NOT NULL UNIQUE,
    description         VARCHAR2(100)   NOT NULL
) TABLESPACE SEC_DATA;

