-- 2. AUDIT LOG TABLE
CREATE TABLE AUDIT_HISTORY (
    audit_id            NUMBER(15)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_name          VARCHAR2(30)    NOT NULL,
    operation           VARCHAR2(10)    NOT NULL, -- INS, UPD, DEL
    record_pk           VARCHAR2(100),  -- Primary Key of affected row
    old_value           VARCHAR2(4000), -- Snapshot before change
    new_value           VARCHAR2(4000), -- Snapshot after change
    changed_by          VARCHAR2(50),   -- User who made the change
    change_timestamp    TIMESTAMP       DEFAULT SYSTIMESTAMP
) TABLESPACE SEC_DATA;

-- Index for Audit Performance
CREATE INDEX idx_audit_table ON AUDIT_HISTORY (table_name, change_timestamp) TABLESPACE SEC_IDX;